const linksContainer = document.getElementById("common-links")

function getLinks() {

    all_links.forEach(link => {
        createLink(link)

    })
}

function createLink(link) {
    const container = document.createElement("div")
    container.id = "link-container"

    const a = document.createElement("a")
    a.href = link[1]
    a.title = `Go to ${link[1]}`

    const title = document.createElement("h1")
    title.classList.add("title")
    title.innerHTML = link[0]
    const kb = document.createElement("button")
    kb.id = "kb"
    kb.tabIndex = 1
    kb.onclick = function() {
        copyKB(this)
    }
    kb.title = "Copy KB"
    kb.innerHTML = link[1].split("=")[1]
    a.appendChild(title)
    container.appendChild(a)
    container.appendChild(kb)

    linksContainer.appendChild(container)
}




function copyKB(button) {

    const name = button.innerHTML
    navigator.clipboard
    .writeText(button.innerHTML)
    .then(function() {
        button.innerHTML = "Copied"
    })
    .catch(function() {
        button.innerHTML = "Failed to Copy"
    })

    setTimeout(() => {
        button.innerHTML = name
    }, 1000);
}

function searchForLink(input) {
    linksContainer.innerHTML = ""

    if (!linksContainer.classList.toggle("no-results")){
        linksContainer.classList.toggle("no-results")
    }

    if (input.value == ""){
        getLinks()
        return
    }

    all_links.forEach(link => {
        if(link[0].toLowerCase().includes(input.value.toLowerCase())) {
            createLink(link)
        }
    })

    if (linksContainer.children.length == 0){
        linksContainer.innerHTML = `No found results for ${input.value}`
        if (!linksContainer.classList.toggle("no-results")){
            linksContainer.classList.toggle("no-results")
        }

    }
}


function summaryPageSetup() {
    const inputField = document.getElementById("link-search")
    inputField.value = ""
    inputField.focus()

    document.addEventListener('keydown', function(event) {
        if (event.ctrlKey && event.key === 'f') {
            event.preventDefault();
            inputField.focus();
        }
    });
    getLinks()
}




